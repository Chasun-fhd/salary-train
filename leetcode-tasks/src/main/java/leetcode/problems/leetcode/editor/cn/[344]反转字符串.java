class Solution344 {

    public void reverseString(char[] s) {
        if (null == s || s.length == 0) return;
        int left = 0, right = s.length - 1;
        while (left < right) {
            char tmp = s[left];
            s[left] = s[right];
            s[right] = tmp;

            left++;
            right--;
        }
    }
}
